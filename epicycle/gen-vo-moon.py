# gen-vo-moon.py — voiceover ไทยสำหรับ MoonPhaseVert (เฟสดวงจันทร์ · ซูเปอร์มูน · ปฏิทินจันทรคติ) · edge-tts
# run: python gen-vo-moon.py
import asyncio, edge_tts, os

VOICE = "th-TH-NiwatNeural"
RATE = "+6%"
OUT = "public/vo-moon"
os.makedirs(OUT, exist_ok=True)

SEGMENTS = [
    "ทำไมดวงจันทร์ ถึงเปลี่ยนรูปร่างทุกคืน",
    "ดวงจันทร์โคจรรอบโลก ราวยี่สิบเก้าวันครึ่ง",
    "รูปร่างที่เปลี่ยน คือด้านสว่างที่เราเห็น มากหรือน้อย",
    "หลายคนคิดว่าจันทร์เสี้ยว เกิดจากเงาโลกบัง แต่นั่นผิด",
    "เงาโลกบังจันทร์ คือจันทรุปราคา ซึ่งนานๆ เกิดที",
    "ดวงจันทร์สว่างที่สุดตอนเพ็ญ และขนาดก็แทบไม่เปลี่ยน",
    "แต่บางครั้ง วงโคจรพาจันทร์เข้าใกล้โลกเป็นพิเศษ",
    "จันทร์เต็มดวง ตอนเข้าใกล้สุด เราเรียกว่าซูเปอร์มูน",
    "คนไทยผูกปฏิทินไว้กับดวงจันทร์มานาน",
    "นับขึ้นและแรม ตามเฟสที่เห็นจริงบนฟ้า",
    "ขึ้นสิบห้าค่ำ ที่เราไหว้พระจันทร์ คือคืนที่จันทร์เต็มดวงพอดี",
]

async def main():
    for i, t in enumerate(SEGMENTS):
        f = f"{OUT}/seg{i:02d}.mp3"
        await edge_tts.Communicate(t, VOICE, rate=RATE).save(f)
        print("✓", f)

asyncio.run(main())
print("เสร็จ —", len(SEGMENTS), "segs ใน", OUT)
