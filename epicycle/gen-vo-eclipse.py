# gen-vo-eclipse.py — voiceover ไทยสำหรับ EclipseStoryVert (อุปราคา/ราหู→เงา→ซารอส) · edge-tts
import asyncio, edge_tts, os
VOICE="th-TH-NiwatNeural"; RATE="+6%"; OUT="public/vo-eclipse"; os.makedirs(OUT,exist_ok=True)
SEGMENTS=[
 "คนโบราณเคยเห็น กลางวันค่อยๆ มืดลง จนดาวโผล่ออกมากลางวัน",
 "เชื่อกันว่า ราหูอมดวงอาทิตย์",
 "แต่ภาพจริงคือ ดวงจันทร์เลื่อนมาบังดวงอาทิตย์พอดี",
 "บังเอิญที่จานดวงจันทร์ กับจานดวงอาทิตย์ ดูโตเท่ากันบนฟ้า จึงบังกันได้สนิท",
 "นี่คือสุริยุปราคา ไม่ใช่ราหู",
 "ราหูกับเกตุ แท้จริงคือจุดตัดของวงโคจร ที่ดวงจันทร์ข้ามระนาบของโลก",
 "อุปราคาจะเกิด ก็ต่อเมื่อดวงจันทร์มาอยู่ตรงจุดนั้นพอดี",
 "ถ้าเงาของโลก ทาบลงบนดวงจันทร์ ก็จะเป็นจันทรุปราคา",
 "และอุปราคาไม่ได้เกิดมั่ว มันวนซ้ำเป็นวงรอบ เรียกว่าซารอส",
 "ทุกๆ ราวสิบแปดปี รูปแบบเดิมก็จะกลับมาอีกครั้ง",
 "สิ่งที่คนโบราณเรียกว่าราหู วันนี้เรารู้แล้วว่า คือเรขาคณิตของเงา",
]
async def main():
    for i,t in enumerate(SEGMENTS):
        fn=f"{OUT}/seg{i:02d}.mp3"; await edge_tts.Communicate(t,VOICE,rate=RATE).save(fn); print("✓",fn)
asyncio.run(main()); print("เสร็จ",len(SEGMENTS),"segs",OUT)
