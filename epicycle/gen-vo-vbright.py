# gen-vo-vbright.py — voiceover ไทยสำหรับ VenusBrightVert (ดาวศุกร์สว่างที่สุด 22 ก.ย. 69) · edge-tts
# มุมใหม่ (ไม่ซ้ำ VenusPhaseVert): ทำไม "เสี้ยว" สว่างกว่า "เต็มดวง" = สมดุล ขนาดจาน × เฟส
import asyncio, edge_tts, os
VOICE="th-TH-NiwatNeural"; RATE="+6%"; OUT="public/vo-vbright"; os.makedirs(OUT,exist_ok=True)
# เรียบเรียงใหม่ "การเดินทาง 6 เดือน" (uniform-time) — ยาวช่วงไต่ขึ้น/เข้าใกล้ + กระชับช่วงสว่างสุด
SEGMENTS=[
 "รองจากดวงจันทร์ ดาวที่สว่างที่สุดบนฟ้ายามค่ำ คือดาวศุกร์",
 "ตอนนี้ดาวศุกร์อยู่ไกล อีกฟากของดวงอาทิตย์ เราจึงเห็นเกือบเต็มดวง แต่ดวงเล็กและไม่สว่างนัก",
 "ดาวศุกร์โคจรรอบดวงอาทิตย์ วงในใกล้กว่าโลก เราจึงเห็นมันมีข้างขึ้นข้างแรม เหมือนดวงจันทร์",
 "เดือนแล้วเดือนเล่า มันค่อยๆ ไต่สูงขึ้นบนท้องฟ้า และโคจรเข้าใกล้โลกมากขึ้น",
 "จานของดาวศุกร์ใหญ่ขึ้นเรื่อยๆ พร้อมกับค่อยๆ แหว่ง จากเต็มดวง เป็นครึ่งดวง และเป็นเสี้ยว",
 "และยิ่งเข้าใกล้ แม้แสงจะเหลือเพียงเสี้ยว แต่จานที่ใหญ่ขึ้นมาก กลับทำให้มันสว่างยิ่งกว่าเดิม",
 "พอเป็นเสี้ยวราวหนึ่งในสี่ ดาวศุกร์ก็สว่างที่สุด ถึงลบสี่จุดห้า สว่างกว่าดาวฤกษ์ที่สุกใสที่สุดสิบหกเท่า จนทอดเงาจางๆ ได้",
 "ช่วงที่สว่างที่สุด คือ ยี่สิบสองกันยายนนี้ หัวค่ำ ทางทิศตะวันตก",
 "ยามค่ำคนไทยเรียกดาวประจำเมือง ยามเช้าเรียกดาวประกายพรึก ที่แท้คือดาวศุกร์ดวงเดียวกัน",
]
async def main():
    for i,t in enumerate(SEGMENTS):
        fn=f"{OUT}/seg{i:02d}.mp3"; await edge_tts.Communicate(t,VOICE,rate=RATE).save(fn); print("OK",fn)
asyncio.run(main()); print("done",len(SEGMENTS),"segs",OUT)
