# gen-vo-merc.py — voiceover ไทยสำหรับ MercuryRetroVert (ดาวพุธพักร) · edge-tts
# run: python gen-vo-merc.py
import asyncio, edge_tts, os

VOICE = "th-TH-NiwatNeural"
RATE = "+6%"
OUT = "public/vo-merc"
os.makedirs(OUT, exist_ok=True)

# ⚠️ ตัดช่องว่างในประโยคออกหมด (16 มิ.ย. 2569) — ช่องว่างทำ edge-tts tokenizer แบ่งคำผิด
# แทรก pause กลางคำ (เช่น "ถอย·หลัง" ไม่ติดกัน) · ปล่อยให้ engine ตัดคำเอง = อ่านลื่นต่อเนื่อง
SEGMENTS = [
    "ทำไมเวลาเขาบอกว่าดาวพุธพักรแล้วใครๆก็พากันกังวล",
    "โหราศาสตร์เชื่อว่าช่วงดาวพุธเดินถอยหลังการสื่อสารและสัญญาจะติดขัด",
    "และบนท้องฟ้าจริงดาวพุธก็ดูเหมือนหยุดแล้วเดินถอยหลังจริงๆ",
    "แต่ดาวพุธเดินถอยหลังจริงหรือเปล่า",
    "ดาวพุธอยู่วงในใกล้ดวงอาทิตย์โคจรเร็วกว่าโลกราวสี่เท่า",
    "พอมันวิ่งแซงโลกผ่านมาอยู่ระหว่างเรากับดวงอาทิตย์",
    "เส้นเล็งจากโลกไปยังดาวพุธที่ทาบกับหมู่ดาวก็เลยวกถอย",
    "เหมือนเวลาเราขับรถแซงอีกคันคันที่ถูกแซงดูเหมือนถอยหลังทั้งที่ยังวิ่งไปข้างหน้า",
    "ดาวพุธก็ไม่ได้เดินถอยจริงสักนิด",
    "มันเป็นแค่ภาพลวงตาจากมุมมองของเราบนโลก",
    "ดาวพุธพักรจึงเป็นเรื่องของเรขาคณิตไม่ใช่ลางร้าย",
]

async def main():
    for i, t in enumerate(SEGMENTS):
        f = f"{OUT}/seg{i:02d}.mp3"
        await edge_tts.Communicate(t, VOICE, rate=RATE).save(f)
        print("✓", f)

asyncio.run(main())
print("เสร็จ —", len(SEGMENTS), "segs ใน", OUT)
