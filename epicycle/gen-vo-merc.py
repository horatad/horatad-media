# gen-vo-merc.py — voiceover ไทยสำหรับ MercuryRetroVert (ดาวพุธพักร) · edge-tts
# run: python gen-vo-merc.py
import asyncio, edge_tts, os

VOICE = "th-TH-NiwatNeural"
RATE = "+6%"
OUT = "public/vo-merc"
os.makedirs(OUT, exist_ok=True)

SEGMENTS = [
    "ทำไมเวลาเขาบอกว่า ดาวพุธพักร แล้วใครๆ ก็พากันกังวล",
    "โหราศาสตร์เชื่อว่า ช่วงดาวพุธเดินถอยหลัง การสื่อสารและสัญญาจะติดขัด",
    "และบนท้องฟ้าจริง ดาวพุธก็ดูเหมือนหยุด แล้วเดินถอยหลังจริงๆ",
    "แต่ดาวพุธ เดินถอยหลังจริงหรือเปล่า",
    "ดาวพุธอยู่วงในใกล้ดวงอาทิตย์ โคจรเร็วกว่าโลกราวสี่เท่า",
    "พอมันวิ่งแซงโลก ผ่านมาอยู่ระหว่างเรากับดวงอาทิตย์",
    "เส้นเล็งจากโลกไปยังดาวพุธ ที่ทาบกับหมู่ดาว ก็เลยวกถอย",
    "เหมือนเวลาเราขับรถแซงอีกคัน คันที่ถูกแซงดูเหมือนถอยหลัง ทั้งที่ยังวิ่งไปข้างหน้า",
    "ดาวพุธก็ไม่ได้เดินถอยจริงสักนิด",
    "มันเป็นแค่ภาพลวงตา จากมุมมองของเราบนโลก",
    "ดาวพุธพักร จึงเป็นเรื่องของเรขาคณิต ไม่ใช่ลางร้าย",
]

async def main():
    for i, t in enumerate(SEGMENTS):
        f = f"{OUT}/seg{i:02d}.mp3"
        await edge_tts.Communicate(t, VOICE, rate=RATE).save(f)
        print("✓", f)

asyncio.run(main())
print("เสร็จ —", len(SEGMENTS), "segs ใน", OUT)
