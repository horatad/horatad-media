# gen-vo-merc.py — voiceover ไทยสำหรับ MercuryRetroVert (ดาวพุธพักร) · edge-tts
# run: python gen-vo-merc.py
import asyncio, edge_tts, os

VOICE = "th-TH-NiwatNeural"
RATE = "+6%"
OUT = "public/vo-merc"
os.makedirs(OUT, exist_ok=True)

# ⚠️ จูน TTS ให้อ่านลื่น (16 มิ.ย. 2569 · พี่ปีเตอร์ฟันธงจากการฟังเทียบ):
#  1) base ตัดช่องว่างออก (ช่องว่างทำ edge-tts ตัดคำผิด แทรก pause กลางคำ)
#  2) "ดาวพุธพักร" → สะกด phonetic "ดาวพุดพัก" (กัน TTS อ่านเป็น "พุด-ทะ-พัก" สับสนกับ พุทธ) · VO เท่านั้น caption ยังเป็น "ดาวพุธพักร"
#  3) ใส่ช่องว่าง "หลัง" คำ "ถอยหลัง" ทุกจุด (ถอยหลัง_xxx) → กันเสียง "ถอย·หลัง" สะดุด
SEGMENTS = [
    "ทำไมเวลาเขาบอกว่าดาวพุดพักแล้วใครๆก็พากันกังวล",
    "โหราศาสตร์เชื่อว่าช่วงดาวพุธเดินถอยหลัง การสื่อสารและสัญญาจะติดขัด",
    "และบนท้องฟ้าจริงดาวพุธก็ดูเหมือนหยุดแล้วเดินถอยหลัง จริงๆ",
    "แต่ดาวพุธเดินถอยหลัง จริงหรือเปล่า",
    "ดาวพุธอยู่วงในใกล้ดวงอาทิตย์โคจรเร็วกว่าโลกราวสี่เท่า",
    "พอมันวิ่งแซงโลกผ่านมาอยู่ระหว่างเรากับดวงอาทิตย์",
    "เส้นเล็งจากโลกไปยังดาวพุธที่ทาบกับหมู่ดาวก็เลยวกถอย",
    "เหมือนเวลาเราขับรถแซงอีกคันคันที่ถูกแซงดูเหมือนถอยหลัง ทั้งที่ยังวิ่งไปข้างหน้า",
    "ดาวพุธก็ไม่ได้เดินถอยจริงสักนิด",
    "มันเป็นแค่ภาพลวงตาจากมุมมองของเราบนโลก",
    "ดาวพุดพักจึงเป็นเรื่องของเรขาคณิตไม่ใช่ลางร้าย",
]

async def main():
    for i, t in enumerate(SEGMENTS):
        f = f"{OUT}/seg{i:02d}.mp3"
        await edge_tts.Communicate(t, VOICE, rate=RATE).save(f)
        print("✓", f)

asyncio.run(main())
print("เสร็จ —", len(SEGMENTS), "segs ใน", OUT)
