# gen-vo-occult.py — voiceover ไทยสำหรับ OccultationVert (ดวงจันทร์บังดาวศุกร์ 14 ก.ย. 69) · edge-tts
import asyncio, edge_tts, os
VOICE="th-TH-NiwatNeural"; RATE="+6%"; OUT="public/vo-occult"; os.makedirs(OUT,exist_ok=True)
SEGMENTS=[
 "คืนวันที่สิบสี่ กันยายน ดวงจันทร์จะเคลื่อนไปบังดาวศุกร์",
 "ดาวศุกร์ที่สว่างที่สุดบนท้องฟ้า จะค่อยๆ หายวับไปหลังดวงจันทร์",
 "ปรากฏการณ์นี้เรียกว่า ดวงจันทร์บังดาวศุกร์",
 "ดวงจันทร์อยู่ใกล้โลกมาก แค่สามแสนแปดหมื่นกิโลเมตร",
 "ส่วนดาวศุกร์อยู่ไกลกว่านั้นเกือบสองร้อยเท่า แต่บนฟ้ากลับดูเหมือนซ้อนกัน",
 "เพราะดวงจันทร์โคจรเร็ว เคลื่อนผ่านท้องฟ้า ไปบังดาวที่อยู่ไกลกว่า",
 "แล้วทำไมเห็นแค่หัวค่ำ ทางทิศตะวันตก",
 "เพราะดาวศุกร์เป็นดาววงใน โคจรใกล้ดวงอาทิตย์เสมอ",
 "มันจึงโผล่ให้เห็นแค่ตอนหัวค่ำ หรือเช้ามืด ใกล้ดวงอาทิตย์",
 "และถ้าดูดีๆ ดาวศุกร์ก็เป็นเสี้ยว เหมือนดวงจันทร์",
 "สิบสี่ กันยายน เริ่มบังหนึ่งทุ่มยี่สิบแปด ทางทิศตะวันตก ดูได้ด้วยตาเปล่า",
]
async def main():
    for i,t in enumerate(SEGMENTS):
        fn=f"{OUT}/seg{i:02d}.mp3"; await edge_tts.Communicate(t,VOICE,rate=RATE).save(fn); print("OK",fn)
asyncio.run(main()); print("เสร็จ",len(SEGMENTS),"segs",OUT)
