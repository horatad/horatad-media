# gen-vo-venus.py — voiceover ไทยสำหรับ VenusPhaseVert/VenusBrightestVert (เฟสดาวศุกร์) · edge-tts
import asyncio, edge_tts, os
VOICE="th-TH-NiwatNeural"; RATE="+6%"; OUT="public/vo-venus"; os.makedirs(OUT,exist_ok=True)
SEGMENTS=[
 "ดาวศุกร์ คือดาวที่สว่างที่สุดบนท้องฟ้ายามค่ำ",
 "หลายคนไม่รู้ว่า ดาวศุกร์ก็มีข้างขึ้นข้างแรม เหมือนดวงจันทร์",
 "เพราะมันโคจรรอบดวงอาทิตย์ อยู่วงในใกล้กว่าโลก",
 "เราเลยเห็นด้านสว่างของมัน มากบ้างน้อยบ้าง",
 "ตอนอยู่ไกล อีกฟากของดวงอาทิตย์ ดาวศุกร์เต็มดวง แต่ดูเล็กและจาง",
 "ตอนเข้าใกล้โลก มันเป็นเสี้ยวบาง แต่จานกลับใหญ่ขึ้นมาก",
 "และเสี้ยวบางนี่เอง ที่ดาวศุกร์สว่างที่สุด",
 "เมื่อสี่ร้อยปีก่อน กาลิเลโอ ส่องกล้องเห็นเฟสเหล่านี้",
 "มันพิสูจน์ว่า ดาวศุกร์โคจรรอบดวงอาทิตย์ ไม่ใช่รอบโลก",
 "ดาวศุกร์ไม่เคยห่างจากดวงอาทิตย์มาก เราจึงเห็นมันแค่หัวค่ำหรือรุ่งเช้า",
 "คนไทยเรียกมันว่า ดาวประจำเมือง และดาวประกายพรึก",
]
async def main():
    for i,t in enumerate(SEGMENTS):
        fn=f"{OUT}/seg{i:02d}.mp3"; await edge_tts.Communicate(t,VOICE,rate=RATE).save(fn); print("✓",fn)
asyncio.run(main()); print("เสร็จ",len(SEGMENTS),"segs",OUT)
