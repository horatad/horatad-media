# gen-vo-opp.py — voiceover ไทยสำหรับ OppositionVert (ดาวเคราะห์ใกล้โลก/opposition) · edge-tts
import asyncio, edge_tts, os
VOICE="th-TH-NiwatNeural"; RATE="+6%"; OUT="public/vo-opp"; os.makedirs(OUT,exist_ok=True)
SEGMENTS=[
 "บางคืน ดาวเสาร์สว่างเป็นพิเศษ และขึ้นมาให้เห็นได้ทั้งคืน",
 "ช่วงนั้นเรียกว่า ดาวเสาร์อยู่ตรงข้ามดวงอาทิตย์",
 "คือโลกโคจรมาอยู่ตรงกลาง ระหว่างดวงอาทิตย์กับดาวเสาร์พอดี",
 "ดาวเสาร์จึงอยู่ใกล้โลกที่สุดในรอบปี",
 "พอใกล้ที่สุด ก็เลยดูใหญ่ที่สุดและสว่างที่สุด",
 "พอดวงอาทิตย์ตก ดาวเสาร์ก็ขึ้นทางตรงข้ามพอดี เห็นได้ทั้งคืน",
 "และช่วงนี้เอง ที่ดาวเสาร์ดูเหมือนเดินถอยหลัง",
 "เพราะโลกที่อยู่วงในกว่า วิ่งแซงมันไปนั่นเอง",
 "โหราศาสตร์เรียกการเดินถอยหลังนี้ว่า พักร",
 "แต่จริงๆ มันคือจังหวะที่ดาวอยู่ใกล้เราที่สุด",
 "ปีนี้ ดาวเสาร์ใกล้โลกที่สุด สี่ตุลาคม สองพันห้าร้อยหกสิบเก้า",
]
async def main():
    for i,t in enumerate(SEGMENTS):
        fn=f"{OUT}/seg{i:02d}.mp3"; await edge_tts.Communicate(t,VOICE,rate=RATE).save(fn); print("✓",fn)
asyncio.run(main()); print("เสร็จ",len(SEGMENTS),"segs",OUT)
