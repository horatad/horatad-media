# gen-vo-smiley.py — voiceover ไทยสำหรับ SmileyFace (จันทร์ยิ้ม) · edge-tts
# run: python gen-vo-smiley.py
import asyncio, edge_tts, os

VOICE = "th-TH-NiwatNeural"
RATE = "+6%"
OUT = "public/vo-smiley"
os.makedirs(OUT, exist_ok=True)

SEGMENTS = [
    "เคยไหม เงยหน้ามองฟ้ายามค่ำ แล้วเหมือนท้องฟ้ายิ้มให้เรา",
    "ดาวสว่างสองดวง คือดวงตา พระจันทร์เสี้ยวด้านล่าง คือรอยยิ้ม",
    "ดวงตาทั้งคู่ คือดาวศุกร์กับดาวพฤหัส สองดาวสว่างที่สุดบนฟ้า",
    "ทั้งสามเรียงใกล้กัน จนดูเป็นใบหน้ายิ้ม ที่เรียกกันว่า พระจันทร์ยิ้ม",
    "แต่จริงๆ ทั้งสามไม่ได้อยู่ใกล้กันเลย",
    "พระจันทร์ห่างเราสามแสนกว่ากิโลเมตร ศุกร์กับพฤหัส ไกลออกไปอีกหลายร้อยเท่า",
    "มันแค่บังเอิญ อยู่ในทิศเดียวกัน เมื่อมองจากโลก",
    "บ้านเราใกล้เส้นศูนย์สูตร พระจันทร์เสี้ยวจึงหงายเป็นเรือ เห็นรอยยิ้มชัด",
    "รอยยิ้มนั้น คือด้านสว่างของพระจันทร์ ที่หันรับแสงอาทิตย์",
    "ดูได้กลางเดือนมิถุนายน สองห้าหกเก้า ทางทิศตะวันตก หลังตะวันตกดิน",
    "โหราศาสตร์เรียกการพบกันนี้ว่า การกุมของดาวศุภเคราะห์ ครั้งหน้าถ้าฟ้ายิ้ม ก็ยิ้มตอบนะ",
]

async def main():
    for i, t in enumerate(SEGMENTS):
        f = f"{OUT}/seg{i:02d}.mp3"
        await edge_tts.Communicate(t, VOICE, rate=RATE).save(f)
        print("OK", f)

asyncio.run(main())
print("done —", len(SEGMENTS), "segs in", OUT)
